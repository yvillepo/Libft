#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;

	if (dst == src || len == 0)
		return (dst);
	d = (unsigned char*)dst;
	s = (unsigned char*)src;
	while (len--)
	{
		*d++ = *s++;
	}
	return (dst);
}
