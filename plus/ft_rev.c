#include "libft.h"

char	*ft_rev(char *str)
{
	char	*sb;
	char	*s;

	s = str;
	sb = str;
	while (*sb)
		sb++;
	sb--;
	while(s < sb)
	{
		ft_swap_c(s, sb);
		s++;
		sb--;
	}
	return (str);
}
